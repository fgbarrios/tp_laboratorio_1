/** \brief SUMA DE OPERANDOS
 *
 * \param num1 int PRIMER OPERANDO
 * \param num2 int SEGUNDO OPERANDO
 * \return int RESULTADO DE LA SUMA
 *
 */
int suma(int num1, int num2);

/** \brief RESTA DE OPERANDOS
 *
 * \param num1 int PRIMER OPERANDO
 * \param num2 int SEGUNDO OPERANDO
 * \return int RESULTADO DE LA RESTA
 *
 */
int resta(int num1, int num2);

/** \brief DIVISION DE OPERANDOS
 *
 * \param num1 int PRIMER OPERANDO
 * \param num2 int SEGUNDO OPERANDO
 * \return float RESULTADO DE LA DIVISION
 *
 */
float dividir(int num1, int num2);

/** \brief MULTIPLICACION DE OPERANDOS
 *
 * \param num1 int PRIMER OPERANDO
 * \param num2 int SEGUNDO OPERANDO
 * \return int RESULTADO DE LA MULTIPLICACION
 *
 */
int multiplicar(int num1, int num2);

/** \brief FACTORIAL DE OPERANDOS
 *
 * \param num int OPERANDO A FACTORIZAR
 * \return int RESULTADO DE LA FACTORIZACION
 *
 */
int factorial(int num);

#endif // BARRIOS_H_INCLUDED


#endif /* BARRIOS_H_ */
